package com.semobang.love.domain;

public class LoveVO {

	private int love_id;
	private String love_user;
	private int love_property;
	
	public int getLove_id() {
		return love_id;
	}
	public void setLove_id(int love_id) {
		this.love_id = love_id;
	}
	public String getLove_user() {
		return love_user;
	}
	public void setLove_user(String love_user) {
		this.love_user = love_user;
	}
	public int getLove_property() {
		return love_property;
	}
	public void setLove_property(int love_property) {
		this.love_property = love_property;
	}
}
